CREATE TABLE exchange_rates (
    id UUID PRIMARY KEY,
    corridor_id UUID NOT NULL REFERENCES corridors(id) ON DELETE CASCADE,
    bp_rate DECIMAL(15, 4) NOT NULL,
    bank_rate DECIMAL(15, 4) NOT NULL,
    spread DECIMAL(15, 4) NOT NULL,
    fee_fixed DECIMAL(15, 2) NOT NULL DEFAULT 0.00,
    fee_percent DECIMAL(5, 2) NOT NULL DEFAULT 0.00,
    valid_until TIMESTAMP NOT NULL,
    fetched_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
